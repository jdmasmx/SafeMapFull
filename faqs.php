                <h3>Where is Cozumel?</h3><br />

                <p>Cozumel is an island in the Caribbean Sea off the eastern coast of Mexico's Yucatan Peninsula, opposite Playa del Carmen, and close to the Yucatan Channel.</p><br />

                <h3>How do I get there?</h3><br />

                <p>Cozumel has an international airport with many direct scheduled flights from cities in the USA, Mexico City, Cancun. Many charter airlines also fly to Cozumel. Alternatively, it is possible to fly to Cancun airport and make the connection to Cozumel by bus and ferry. The journey takes 1.5 to 2.5 hours depending on waiting times (45 min by bus to Playa del Carmen and another 45 min by ferry to Cozumel). The cheapest bus operator from Cancun airport to Playa del Carmen is Riviera, tickets can be purchased in the main airport terminal and the buses are clean and efficient. There are two ferry operators but there is little difference in service or prices between the two, it's best to simply take whichever one is leaving next.</p><br />


                <h3>Do I need a visa?</h3><br />

                <p>Not usually. Most North Americans and Europeans can enter Mexico with a tourist card – valid for up to 6 months - which you get by simply filling out a form on the airplane or at the arrival airport. However, rules can change, so check with your local Mexican embassy or consulate if you are not sure or if you are visiting for more than six months or for a purpose other than vacation.</p><br />


                <h3>What currency is used?</h3><br />

                <p>Mexican pesos and US dollars (bills, not usually coins) are accepted in Cozumel. You may get a better deal paying in pesos since you won't lose on the exchange rate. Major credit cards and travelers' checks are also widely accepted and most cards will also work in Mexican ATMs.</p><br />


                <h3>What language is spoken?</h3><br />

                <p>Spanish is the official language of Mexico and is spoken everywhere in Cozumel. However, due to tourism English is also very widely spoken in Cozumel and non-Spanish speakers should have no problem getting by. You may also here some locals speaking Mayan, the ancient native language of the region.</p><br />


                <h3>What time zone is Cozumel in?</h3><br />

                <p>Standard time in Cozumel is the same as Cancun: GMT minus 6 hours or minus 5 with daylight saving. This means it usually has the same time as Chicago, Dallas and Winnipeg.</p><br />


                <h3>Are there any dangers?</h3><br />

                <p>Crime is low in Cozumel due to it being an island community dependent on tourism. As with anywhere with large groups of tourists it is always wise to keep money and valuables safe from pickpockets and petty thieves, but violent crime is extremely rare. Some biting insects such as mosquitoes and sand flies can be an annoyance, but do not carry diseases such as malaria. They can easily be avoided by using repellent and wearing long sleeves and pants at dawn and dusk and using air conditioning, fans or insect nets to sleep.</p><br />


                <h3>How is the climate?</h3><br />

                <p>With a subtropical climate the average temperature is about 27°C - 80°F, and because Cozumel is surrounded by water the climate is always humid often around 90%, but often breezy too. All year round you will be able to find sunny days with the usually warm waters. Rainfall is rare apart from during the rainy season (September- November) when showers can be frequent but not disruptive.</p><br />



