                <h3>¿Donde está Cozumel?</h3><br />

                <p>Cozumel es una isla en el Mar Caribe frente a la costa oriental de la Península de Yucatán en México, frente a Playa del Carmen, y cerca del Canal de Yucatán.</p><br />

                <h3>¿Como Llegar?</h3><br />

                <p>Cozumel cuenta con un aeropuerto internacional con muchos vuelos directos regulares desde las ciudades en los EE.UU., Ciudad de México, Cancún. Muchas líneas aéreas chárter también vuelan a Cozumel.
                <br />Alternativamente, es posible volar al aeropuerto de Cancún y realizar la conexión a Cozumel en autobús y ferry. El trayecto es de 1,5 a 2,5 horas, dependiendo de los tiempos de espera (45 minutos en bus de Playa del Carmen y otro de 45 minutos en ferry a Cozumel). El operador más barato en autobús desde el aeropuerto de Cancún a Playa del Carmen es la Riviera, los boletos pueden ser comprados en la terminal principal del aeropuerto y los autobuses están limpios y eficientes. Hay dos operadores de transbordadores, pero hay muy poca diferencia en el servicio o los precios entre los dos, lo mejor es simplemente tomar lo que uno se va al lado.</p><br />


                <h3>¿Necesito una VISA?</h3><br />

                <p>Por lo general no. La mayoría de los norteamericanos y los europeos pueden entrar a México con una tarjeta de turista - válido para un máximo de 6 meses - que se obtiene simplemente rellenando un formulario en el avión o en el aeropuerto de llegada. Sin embargo, las reglas pueden cambiar, así que consulte con su distribuidor local embajada o consulado mexicano si no está seguro o si usted está visitando por más de seis meses o para un propósito que no sea de vacaciones.</p><br />


                <h3>¿Que moneda se utiliza?</h3><br />

                <p>Pesos mexicanos y dólares de los EE.UU. (las facturas no, por lo general las monedas) son aceptadas en Cozumel. Usted puede conseguir una oferta mejor pagar en pesos ya que no se pierde en el tipo de cambio. Las principales tarjetas de crédito y cheques de viajero también son ampliamente aceptadas y la mayoría de las tarjetas también funcionan en los cajeros automáticos de México.</p><br />


                <h3>What language is spoken?</h3><br />

                <p>Spanish is the official language of Mexico and is spoken everywhere in Cozumel. However, due to tourism English is also very widely spoken in Cozumel and non-Spanish speakers should have no problem getting by. You may also here some locals speaking Mayan, the ancient native language of the region.</p><br />


                <h3>¿Cual es la zona horaria de Cozumel?</h3><br />

                <p>El tiempo estándar de Cozumel es el mismo que Cancún: hora GMT menos 6 o menos 5 con el horario de verano. Esto significa que por lo general tiene el mismo tiempo como El Distrito Federal, Guadalajara, Monterrey, Chicago, Dallas y Winnipeg.</p><br />


                <h3>¿Existe algun peligro?</h3><br />

                <p>La delincuencia es baja en Cozumel debido a que es una comunidad de la isla depende del turismo. Al igual que con cualquier lugar con grandes grupos de turistas que siempre es aconsejable guardar el dinero y objetos de valor a salvo de los carteristas y ladrones de poca monta, pero el crimen violento es extremadamente rara. Algunos insectos, como mosquitos y moscas de la arena puede ser una molestia, pero no transmiten enfermedades como la malaria. Ellos pueden ser fácilmente evitados mediante el uso de camisas de manga larga y pantalones y repelente de mosquitos al amanecer y al atardecer y el uso de aire acondicionado, ventiladores o las redes de los insectos al dormir.</p><br />


                <h3>¿Cual es el clima?</h3><br />

                <p>Con un clima subtropical, la temperatura media es de unos 27 ° C - 80 ° F, y debido a Cozumel está rodeado por el agua, el clima es un clima húmedo a menudo alrededor del 90%, pero a menudo demasiado viento. Durante todo el año usted podrá encontrar los días de sol con las aguas cálidas por lo general. Las precipitaciones son poco frecuentes, aparte de la época de lluvias (septiembre-noviembre), en donde lluvias pueden ser frecuentes, pero no perjudicial.</p><br />



