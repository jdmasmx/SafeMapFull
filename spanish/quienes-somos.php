                <p>Safe Cozumel Map ofrece al turista experimenta para disfrutar de los mejores servicios en Cozumel, y es la mejor opción para los proveedores de servicios locales para aumentar su negocio.
                    <br /><br />
                    Además como miembros de una familia hoy separada y distante, estamos cercas y unidos en una misión, un sueño y una realidad; apoyando así a uno de nuestros miembros Arturo Díaz, que tiene 25 de los últimos 27 años de su vida en la Riviera Maya; primero en Cancún por 15 años, después 2 años en el D.F., Ensenada, Puerto Vallarta y recorriendo Islas del Caribe, Centro América y Europa; Nuevamente tres años mas en Cancún y dos más en Playa del Carmen. Pero en los últimos 5 años echando raíces en la maravillosa isla de Cozumel, considerado por Jaques Cousteau como el mejor lugar del mundo. Con Arrecifes coralíferos, que con ello y otras de sus maravillas naturales y su gente, se ha convertido en un polo de desarrollo turístico, y en donde además se practican técnicas terapéuticas muy importantes, como son la delfinoterapia, la equino terapia, y en donde ahora existe en la municipalidad un CRIM que sirve de gran ayuda y apoya a personas con diversas necesidades terapéuticas y de atención, física, medica y/o psicológica. Todo ello nos acerca a las raíces de una institución dirigida de sus miembros y que alcanzo su registro ante el CONACYT como Institución de Asesoría Psicopedagógica para niños que pudieran ser insertados a planes de estudio junto con cualquier otro niño de su edad, en edad preescolar y educación primaria, y que después de más de 25 años de servicio recibió el apoya gubernamental para la continuidad de sus servicios. Con todo ello el otro miembro, joven, después de terminar su Carrera de medicina decide incursionar en los Estados Unidos de América, en la atención de familiar de personas que han sufrido graves quemaduras y danos físicos importantes, cuyos procesos de atención, sanación y recuperación, que además de costosos, son en periodos de tiempo muy largos y con múltiples operaciones y tratamientos médicos. Así también decide incursionar con agencias que brindan el servicio que les contrata el Gobierno Americano a través de la oficina del Seguro Social para la atención a todo tipo de personas con algún tipo de discapacidad o atención especial, en tres aéreas básicas: <strong>cuidado, soporte y acompañamiento, atención y búsqueda de mejoría.</strong>
                    <br /><br />
                    <img src="http://www.safetourscozumel.com/img/contact/mardi.PNG" class="left">
                    Además de estas agencias existen múltiples agrupaciones, clínicas y despachos dedicados a un gran número de servicios especificaos y altamente profesionales que hoy en día enorgullecen la ciencia médica y brindan mejores oportunidades de vida a las personas con discapacidad. Es ahí donde surge MARDI LLC, como una organización cuya misión es ser un instrumento que coadyuve para que un segmento de personas con discapacidad, además de los servicios asistencionales directos, cuenten con espacios de socialización y ayuda mutua, e igualmente puedan tener a su disponibilidad la alternativa de realizar viajes u otras actividades recreativas, culturales y/o deportivas por mencionar algunas, otrora nunca tuvieron o fueron muy ocasionales pero además vinculando parte de estas actividades con algunos miembros de su familia, para generar nuevos lazos y momentos de convivencia, pero en donde las personas con deshabilitad, sean atendidas en lugares específicos temporalmente y de manera simultánea o alterna, y puedan convivir con miembros de su familia, sin que descanse en estos últimos su atención y cuidado.
                    <br /><br />
                    Logrando con ello momentos de vida de gran calidad y cercanía, donde ambas partes saldrán con beneficio, de ahí que para construir, también estamos consientes y convencidos que tendremos que dar el 10 % de todo lo que ingresa por la parte de reservaciones de todos aquellos polos turísticos en donde MARDI LLC sea un impulso de una organización local como Safe Tours Cozumel, que sería la encargada de manera local de seleccionar dentro de los mejores prestadores de servicio a aquellos que se puedan ofertan dentro de los posibles visitantes y que además eventual mente se organicen viajes o reuniones en que se puedan vincular para un mejor viaje y calidad de vida a personas con discapacidad junto con otras personas con causas o circunstancias similares y/o a personas con discapacidad y sus propios familiares pero que ahora todos son objeto de entretenimiento, paseo y diversión, y ninguno es sujeto que deba de ocuparse en la atención del otro miembro de la familia, pues para ello habrá profesionistas o voluntarios encargados de la atención que se requiera, sabemos por las experiencias de Luis Diaz, de como por años se organiza cada año campamentos para 80 0 100 niños con quemaduras, que son atendidos durante una semana por más de 200 voluntarios; igualmente tenemos conocimiento como se lleva a surfear a mas de 50 personas que utilizan sillas de ruedas, se requirieron a mas de 200 voluntarios, para llevar a cabo una competencia. La riqueza humana que ello genera no tiene precio ni …… así que consideramos que ya tenemos todos los conocimientos y recursos básico y disposición, para dar principio a la gran travesía y ya de arranque contamos con la organización de más de una docena de actividades locales terrestres y marítimas en la isla de Cozumel, y más de 15 actividades en sus alrededores, y con ello sabemos que podemos dar servicios en una amplia gama de presupuestos y disponibilidades incluyendo al viajero más exigente o con servicios VIP.
                    <br /><br />
                    Igualmente podemos afirmar que ya desde hace dos años se hicieron las instalaciones necesarias para lo que próximamente será el arranque de viajes de algunas personas con discapacidad y/o que utilizan sillas de ruedas. Pero en esto seremos más cautos y exigentes por seguridad y responsabilidad, igualmente en algunos meses podremos iniciar actividades conjuntas de viajes de personas con discapacidad y sus familiares. Pero en ello sabemos que habremos de requerir de un gran contingente de miembros, ya sean profesionistas y voluntarios, que juntos nos lleven al desarrollo de tan encomiable aventura. Pero dónde la seguridad será el eje básico de desarrollo y ya la diversión y experiencias de crecimiento humano se desprenderán como las caídas de agua d las cascadas.
